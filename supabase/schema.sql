-- Supabase Postgres schema + RLS for YA Dormitory System
-- Apply in Supabase SQL editor (in order).

-- 1) Extensions
create extension if not exists pgcrypto;

-- 2) Profiles (maps to auth.users)
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text unique,
  full_name text,
  email text,
  phone text,
  address text,
  emergency_contact text,
  is_admin boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create or replace function public.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end; $$;

drop trigger if exists trg_profiles_updated_at on public.profiles;
create trigger trg_profiles_updated_at
before update on public.profiles
for each row execute function public.set_updated_at();

-- 3) Core tables
create table if not exists public.rooms (
  id bigint generated by default as identity primary key,
  room_no text not null unique,
  floor_no int not null,
  capacity int not null default 0,
  status text not null default 'Available',
  created_at timestamptz not null default now()
);

create table if not exists public.beds (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.rooms(id) on delete cascade,
  bed_no text not null,
  status text not null default 'Available', -- Available | Reserved | Occupied
  reserved_at timestamptz,
  created_at timestamptz not null default now(),
  unique(room_id, bed_no)
);

create table if not exists public.bookings (
  id bigint generated by default as identity primary key,
  booking_ref text unique,
  user_id uuid references public.profiles(id) on delete set null,
  bed_id bigint references public.beds(id) on delete set null,
  full_name text,
  category text,
  school_name text,
  contact_number text,
  guardian_name text,
  guardian_contact text,
  payment_method text,
  receipt_path text,
  booking_status text not null default 'Pending',
  payment_status text not null default 'Pending',
  monthly_rent numeric(10,2) not null default 0,
  current_balance numeric(10,2) not null default 0,
  move_in_date date,
  move_out_date date,
  due_date date,
  remarks text,
  created_at timestamptz not null default now()
);

create index if not exists idx_bookings_user on public.bookings(user_id);
create index if not exists idx_bookings_bed on public.bookings(bed_id);

create table if not exists public.payments (
  id bigint generated by default as identity primary key,
  booking_id bigint not null references public.bookings(id) on delete cascade,
  amount numeric(10,2) not null default 0,
  payment_method text,
  notes text,
  receipt_path text,
  paid_at timestamptz not null default now()
);

create index if not exists idx_payments_booking on public.payments(booking_id);

create table if not exists public.transfer_requests (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  booking_id bigint not null references public.bookings(id) on delete cascade,
  requested_floor int not null,
  requested_room_id bigint not null references public.rooms(id) on delete cascade,
  requested_bed_no text,
  reason text not null,
  status text not null default 'Pending',
  created_at timestamptz not null default now()
);

create table if not exists public.request_out_requests (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  booking_id bigint not null references public.bookings(id) on delete cascade,
  request_out_date date not null,
  reason text not null,
  status text not null default 'Pending',
  reviewed_by uuid references public.profiles(id) on delete set null,
  reviewed_at timestamptz,
  created_at timestamptz not null default now()
);

-- Chat tables
create table if not exists public.chats (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  last_message text,
  updated_at timestamptz not null default now()
);

create index if not exists idx_chats_user on public.chats(user_id);

create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  chat_id bigint not null references public.chats(id) on delete cascade,
  sender_type text not null, -- user | admin
  message text,
  file_url text,
  is_voice boolean not null default false,
  created_at timestamptz not null default now()
);

create index if not exists idx_messages_chat on public.messages(chat_id);

-- 4) Helpers: admin check
create or replace function public.is_admin()
returns boolean
language sql
stable
as $$
  select coalesce((select is_admin from public.profiles where id = auth.uid()), false)
$$;

