-- 7) Consistency trigger: keep booking display fields aligned with profile (best-effort)
create or replace function public.sync_bookings_from_profile()
returns trigger language plpgsql as $$
begin
  update public.bookings
     set full_name = new.full_name,
         contact_number = new.phone
   where user_id = new.id;
  return new;
end; $$;

drop trigger if exists trg_profile_sync_bookings on public.profiles;
create trigger trg_profile_sync_bookings
after update of full_name, phone on public.profiles
for each row execute function public.sync_bookings_from_profile();

