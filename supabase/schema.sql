-- 6) RLS policies

-- Profiles
drop policy if exists "profiles_select_own_or_admin" on public.profiles;
create policy "profiles_select_own_or_admin"
on public.profiles for select
using (auth.uid() = id or public.is_admin());

drop policy if exists "profiles_update_own_or_admin" on public.profiles;
create policy "profiles_update_own_or_admin"
on public.profiles for update
using (auth.uid() = id or public.is_admin())
with check (auth.uid() = id or public.is_admin());

drop policy if exists "profiles_insert_self" on public.profiles;
create policy "profiles_insert_self"
on public.profiles for insert
with check (auth.uid() = id);

-- Rooms/Beds: readable to all authenticated users; writable by admin
drop policy if exists "rooms_select_auth" on public.rooms;
create policy "rooms_select_auth"
on public.rooms for select
using (auth.role() = 'authenticated');

drop policy if exists "rooms_write_admin" on public.rooms;
create policy "rooms_write_admin"
on public.rooms for all
using (public.is_admin())
with check (public.is_admin());

drop policy if exists "beds_select_auth" on public.beds;
create policy "beds_select_auth"
on public.beds for select
using (auth.role() = 'authenticated');

drop policy if exists "beds_write_admin" on public.beds;
create policy "beds_write_admin"
on public.beds for all
using (public.is_admin())
with check (public.is_admin());

-- Bookings: users see own; admin sees all
drop policy if exists "bookings_select_own_or_admin" on public.bookings;
create policy "bookings_select_own_or_admin"
on public.bookings for select
using (public.is_admin() or user_id = auth.uid());

drop policy if exists "bookings_insert_own_or_admin" on public.bookings;
create policy "bookings_insert_own_or_admin"
on public.bookings for insert
with check (public.is_admin() or user_id = auth.uid());

drop policy if exists "bookings_update_admin_or_own" on public.bookings;
create policy "bookings_update_admin_or_own"
on public.bookings for update
using (public.is_admin() or user_id = auth.uid())
with check (public.is_admin() or user_id = auth.uid());

-- Payments: user can read payments for own bookings; admin all. Inserts/updates admin only (or via server route using service key).
drop policy if exists "payments_select_own_or_admin" on public.payments;
create policy "payments_select_own_or_admin"
on public.payments for select
using (
  public.is_admin() or
  exists (select 1 from public.bookings b where b.id = payments.booking_id and b.user_id = auth.uid())
);

drop policy if exists "payments_write_admin" on public.payments;
create policy "payments_write_admin"
on public.payments for all
using (public.is_admin())
with check (public.is_admin());

-- Transfer requests: user own; admin all
drop policy if exists "transfer_select_own_or_admin" on public.transfer_requests;
create policy "transfer_select_own_or_admin"
on public.transfer_requests for select
using (public.is_admin() or user_id = auth.uid());

drop policy if exists "transfer_insert_own" on public.transfer_requests;
create policy "transfer_insert_own"
on public.transfer_requests for insert
with check (user_id = auth.uid());

drop policy if exists "transfer_update_admin" on public.transfer_requests;
create policy "transfer_update_admin"
on public.transfer_requests for update
